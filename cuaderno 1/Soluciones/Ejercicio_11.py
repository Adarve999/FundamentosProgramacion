grados_fahrenheit = float(input('Introduce la temperatura en grados Fahrenheit: '))
grados_centrigrados = (grados_fahrenheit - 32) * 5 / 9
print (f'{grados_fahrenheit} ºF son {grados_centrigrados:.1f} ºC')
